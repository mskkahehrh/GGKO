async function extractMessageBody(msg, sessionNumber) {
  if (!msg || !msg.message) return '';

  const type = getContentType(msg.message);
  let body = '';

  try {
    switch (type) {
      case 'conversation':
        body = msg.message.conversation;
        break;

      case 'extendedTextMessage':
        body = msg.message.extendedTextMessage?.text;
        break;

      case 'imageMessage':
        body = msg.message.imageMessage?.caption;
        break;

      case 'videoMessage':
        body = msg.message.videoMessage?.caption;
        break;

      case 'buttonsResponseMessage':
        body = msg.message.buttonsResponseMessage?.selectedButtonId;
        break;

      case 'listResponseMessage':
        body = msg.message.listResponseMessage?.singleSelectReply?.selectedRowId;
        break;

      case 'interactiveResponseMessage':
        try {
          const json = msg.message.interactiveResponseMessage?.nativeFlowResponseMessage?.paramsJson;
          body = json ? JSON.parse(json)?.id : '';
        } catch {
          body = '';
        }
        break;

      case 'viewOnceMessage':
        body = msg.message.viewOnceMessage?.message?.conversation || '';
        break;

      case 'viewOnceMessageV2':
        body =
          msg.message.viewOnceMessageV2?.message?.imageMessage?.caption ||
          msg.message.viewOnceMessageV2?.message?.videoMessage?.caption ||
          '';
        break;
    }

    if (typeof body !== 'string') body = '';

    // 🔢 Number system injection
    const ns = numberSystems.get(sessionNumber);
    if (ns?.handleNumberReply) {
      try {
        const injected = await ns.handleNumberReply(msg, body);
        if (typeof injected === 'string' && injected) body = injected;
      } catch (e) {
        console.error(`NumberSystem(${sessionNumber})`, e);
      }
    }

    return body.trim();
  } catch (err) {
    console.error('extractMessageBody error:', err);
    return '';
  }
}
